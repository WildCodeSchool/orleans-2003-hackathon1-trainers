<?php


namespace App\Entity;


class Artwork
{
    private $id;
    private $year;
    private $image;
    private $category;
    private $title;
    private $country;
    private $artist;

    public function __construct(?array $data)
    {
        if(!empty($data)) {
            $this->hydrate($data);
        }
    }

    public function hydrate (array $data) :void
    {
        $this->setId($data['objectID']);
        $this->setYear($data['objectDate']);
        $this->setImage($data['primaryImageSmall']);
        $this->setCategory($data['medium']);
        $this->setTitle($data['title']);
        $this->setCountry($data['country']);
        $this->setArtist($data['artistDisplayName']);
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id): void
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getYear()
    {
        return $this->year;
    }

    /**
     * @param mixed $year
     */
    public function setYear($year): void
    {
        $this->year = $year;
    }

    /**
     * @return mixed
     */
    public function getImage()
    {
        return $this->image;
    }

    /**
     * @param mixed $image
     */
    public function setImage($image): void
    {
        $this->image = $image;
    }

    /**
     * @return mixed
     */
    public function getCategory()
    {
        return $this->category;
    }

    /**
     * @param mixed $category
     */
    public function setCategory($category): void
    {
        $this->category = $category;
    }

    /**
     * @return mixed
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param mixed $title
     */
    public function setTitle($title): void
    {
        $this->title = $title;
    }

    /**
     * @return mixed
     */
    public function getCountry()
    {
        return $this->country;
    }

    /**
     * @param mixed $country
     */
    public function setCountry($country): void
    {
        $this->country = $country;
    }

    /**
     * @return mixed
     */
    public function getArtist()
    {
        return $this->artist;
    }

    /**
     * @param mixed $artist
     */
    public function setArtist($artist): void
    {
        $this->artist = $artist;
    }


}
